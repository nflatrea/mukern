/*
arch/x86_64/trap.S

32 exception stubs + common entry.
Each stub normalises the stack to [vector][errcode][cpu frame], pushes
the GPRs, and calls x86_trap(struct trapframe *).
*/

.macro ISR_NOERR n
.global isr\n
isr\n:
    pushq $0          /* dummy error code */
    pushq $\n         /* vector */
    jmp isr_common
.endm

.macro ISR_ERR n
.global isr\n
isr\n:
    pushq $\n         /* CPU already pushed the error code */
    jmp isr_common
.endm

    ISR_NOERR 0
    ISR_NOERR 1
    ISR_NOERR 2
